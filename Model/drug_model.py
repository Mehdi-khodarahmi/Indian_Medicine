class DrugModel:
    def __init__(self, drug_id, category_id, product_name, price, company_id, description, count):
        self.DrugId = drug_id
        self.CategoryId = category_id
        self.ProductName = product_name
        self.Price = price
        self.CompanyId = company_id
        self.Description = description
        self.Count = count
